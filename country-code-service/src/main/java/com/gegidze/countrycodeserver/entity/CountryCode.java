package com.gegidze.countrycodeserver.entity;

public class CountryCode {
    private int code;
    private String country;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
}
